export const getCountries = (level: Level) => {
  let countryPool: string[] = [];

  switch (level) {
    case Level.One:
      countryPool = EASIEST_COUNTRIES;
      break;
    default:
      countryPool = COUNTRIES;
      break;
  }

  const randomSample = sampleSize(countryPool, SAMPLE_SIZE);
  return randomSample;
};
